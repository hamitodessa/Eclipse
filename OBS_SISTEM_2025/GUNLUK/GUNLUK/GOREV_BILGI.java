package GUNLUK;

public class GOREV_BILGI {
	String tARIH ;
	String sAAT ;
	String iSIM ;
	String gOREV ;
	String mESAS ;
	public GOREV_BILGI(String tARIH, String sAAT, String iSIM, String gOREV, String mESAS) {
		super();
		this.tARIH = tARIH;
		this.sAAT = sAAT;
		this.iSIM = iSIM;
		this.gOREV = gOREV;
		this.mESAS = mESAS;
	}
	public String gettARIH() {
		return tARIH;
	}
	public void settARIH(String tARIH) {
		this.tARIH = tARIH;
	}
	public String getsAAT() {
		return sAAT;
	}
	public void setsAAT(String sAAT) {
		this.sAAT = sAAT;
	}
	public String getiSIM() {
		return iSIM;
	}
	public void setiSIM(String iSIM) {
		this.iSIM = iSIM;
	}
	public String getgOREV() {
		return gOREV;
	}
	public void setgOREV(String gOREV) {
		this.gOREV = gOREV;
	}
	public String getmESAS() {
		return mESAS;
	}
	public void setmESAS(String mESAS) {
		this.mESAS = mESAS;
	}
	
	

}
